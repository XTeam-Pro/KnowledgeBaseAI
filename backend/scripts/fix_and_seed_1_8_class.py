def _merge_node(session, label: str, uid: str, props: dict) -> None:
    all_props = {
        "uid": uid,
        "type": label,
        "tenant_id": TENANT_ID,
        "lifecycle_status": "ACTIVE",
        "updated_at": NOW_MS,
        **props,
    }
    q = (
        f"MERGE (n:{label} {{uid: $uid, tenant_id: $tid}}) "
        "SET n += $props"
    )
    session.run(q, uid=uid, tid=TENANT_ID, props=all_props)


def _merge_rel(session, from_uid: str, rel_type: str, to_uid: str) -> None:
    q = (
        f"MATCH (a {{uid: $from_uid, tenant_id: $tid}}), (b {{uid: $to_uid, tenant_id: $tid}}) "
        f"MERGE (a)-[:{rel_type}]->(b)"
    )
    session.run(q, from_uid=from_uid, to_uid=to_uid, tid=TENANT_ID)


def _delete_wrong_skill_rels(session, topic_uid: str, allowed_skill_uids: list[str], dry_run: bool) -> int:
    """
    Удаляет REQUIRES_SKILL-рёбра у темы, если навык НЕ входит в allowed_skill_uids.
    Возвращает количество удалённых рёбер.
    """
    wrong_skills = session.run(
        """
        MATCH (t:Topic {uid: $uid, tenant_id: $tid})-[:REQUIRES_SKILL]->(sk:Skill)
        WHERE NOT sk.uid IN $allowed
        RETURN sk.uid AS uid, sk.title AS title
        """,
        uid=topic_uid,
        tid=TENANT_ID,
        allowed=allowed_skill_uids,
    ).data()

    if not wrong_skills:
        return 0

    for s in wrong_skills:
        print(f"      🗑️  Удаляю связь: тема {topic_uid} → навык {s['uid']} ({s['title']})")

    if not dry_run:
        session.run(
            """
            MATCH (t:Topic {uid: $uid, tenant_id: $tid})-[r:REQUIRES_SKILL]->(sk:Skill)
            WHERE NOT sk.uid IN $allowed
            DELETE r
            """,
            uid=topic_uid,
            tid=TENANT_ID,
            allowed=allowed_skill_uids,
        )

    return len(wrong_skills)


