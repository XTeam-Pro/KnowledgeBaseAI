def seed(dry_run: bool = False, target_topic_uid: str | None = None) -> None:
    repo = Neo4jRepo()
    drv = repo.driver

    stats = {
        "topics": 0,
        "skills": 0,
        "methods": 0,
        "examples": 0,
        "rels": 0,
        "deleted_rels": 0,
    }

    data = DATA
    if target_topic_uid:
        data = [e for e in DATA if e["topic"]["uid"] == target_topic_uid]
        if not data:
            print(f"Тема {target_topic_uid} не найдена в данных скрипта.")
            return

    try:
        with drv.session() as session:
            for entry in data:
                topic = entry["topic"]
                topic_uid = topic["uid"]
                topic_props = {k: v for k, v in topic.items() if k != "uid"}

                print(f"\n[Topic] {topic_uid}: {topic.get('title', '')}")

                if not dry_run:
                    _merge_node(session, "Topic", topic_uid, topic_props)
                stats["topics"] += 1

                # Собираем uid ДОПУСТИМЫХ навыков для этой темы
                allowed_skill_uids = [skill["uid"] for skill in entry.get("skills", [])]

                # Удаляем нерелевантные связи если тема в белом списке или данные задают полный список навыков
                if topic_uid in ALLOWED_SKILLS_FOR_TOPIC:
                    explicit_allowed = ALLOWED_SKILLS_FOR_TOPIC[topic_uid]
                    # объединяем: явный список + те что в DATA
                    merged_allowed = list(set(explicit_allowed + allowed_skill_uids))
                    print(f"  ⚙️  Очищаю нерелевантные навыки (разрешены: {merged_allowed})")
                    deleted = _delete_wrong_skill_rels(session, topic_uid, merged_allowed, dry_run)
                    stats["deleted_rels"] += deleted

                for skill in entry.get("skills", []):
                    skill_uid = skill["uid"]
                    skill_props = {k: v for k, v in skill.items() if k not in ("uid", "methods")}

                    print(f"  [Skill] {skill_uid}: {skill.get('title', '')}")

                    if not dry_run:
                        _merge_node(session, "Skill", skill_uid, skill_props)
                        _merge_rel(session, topic_uid, "REQUIRES_SKILL", skill_uid)
                    stats["skills"] += 1
                    stats["rels"] += 1

                    for method in skill.get("methods", []):
                        method_uid = method["uid"]
                        method_props = {k: v for k, v in method.items() if k not in ("uid", "examples")}

                        print(f"    [Method] {method_uid}: {method.get('title', '')}")

                        if not dry_run:
                            _merge_node(session, "Method", method_uid, method_props)
                            _merge_rel(session, skill_uid, "HAS_METHOD", method_uid)
                        stats["methods"] += 1
                        stats["rels"] += 1

                        for example in method.get("examples", []):
                            ex_uid = example["uid"]
                            ex_props = {k: v for k, v in example.items() if k != "uid"}

                            if not dry_run:
                                _merge_node(session, "Example", ex_uid, ex_props)
                                _merge_rel(session, method_uid, "HAS_EXAMPLE", ex_uid)
                            stats["examples"] += 1
                            stats["rels"] += 1

    finally:
        repo.close()

    mode = "(DRY RUN)" if dry_run else "(ПРИМЕНЕНО)"
    print()
    print("=" * 60)
    print(f"Seed 1–8 класс завершён {mode}")
    print(f"  Темы     : {stats['topics']}")
    print(f"  Навыки   : {stats['skills']}")
    print(f"  Методы   : {stats['methods']}")
    print(f"  Примеры  : {stats['examples']}")
    print(f"  Связи+   : {stats['rels']}")
    print(f"  Удалено  : {stats['deleted_rels']} нерелевантных связей")
    print("=" * 60)


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Fix & Seed: Математика 1–8 класс")
    parser.add_argument("--dry-run", action="store_true", help="Только показать, не применять")
    parser.add_argument("--topic", default=None, metavar="UID", help="Обработать только одну тему")
    args = parser.parse_args()

    print("Fix & Seed: Математика 1–8 класс (ФГОС)")
    print(f"Режим: {'DRY RUN' if args.dry_run else 'WRITE'}")
    if args.topic:
        print(f"Тема: {args.topic}")
    print("=" * 60)
    seed(dry_run=args.dry_run, target_topic_uid=args.topic)
