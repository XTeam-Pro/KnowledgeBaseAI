def _merge_node(session, label: str, uid: str, props: dict, tenant_id: str) -> None:
    all_props = {
        "uid": uid,
        "type": label,
        "tenant_id": tenant_id,
        "lifecycle_status": "ACTIVE",
        "updated_at": NOW_MS,
        **props,
    }
    q = (
        f"MERGE (n:{label} {{uid: $uid, tenant_id: $tenant_id}}) "
        "SET n += $props"
    )
    session.run(q, uid=uid, tenant_id=tenant_id, props=all_props)


def _merge_rel(session, from_uid: str, rel_type: str, to_uid: str, tenant_id: str) -> None:
    q = (
        f"MATCH (a {{uid: $from_uid, tenant_id: $tid}}), (b {{uid: $to_uid, tenant_id: $tid}}) "
        f"MERGE (a)-[:{rel_type}]->(b)"
    )
    session.run(q, from_uid=from_uid, to_uid=to_uid, tid=tenant_id)


def _process_exam_data(session, data: list, exam_label: str, dry_run: bool):
    stats = {"topics": 0, "skills": 0, "methods": 0, "examples": 0, "rels": 0}

    for entry in data:
        topic = entry["topic"]
        topic_uid = topic["uid"]

        if not dry_run:
            topic_props = {k: v for k, v in topic.items() if k != "uid"}
            _merge_node(session, "Topic", topic_uid, topic_props, TENANT_ID)
        stats["topics"] += 1
        print(f"  [Topic] {topic_uid}")
        print(f"    {topic.get('title', '')}")

        for skill in entry.get("skills", []):
            skill_uid = skill["uid"]
            skill_props = {k: v for k, v in skill.items() if k not in ("uid", "methods")}

            if not dry_run:
                _merge_node(session, "Skill", skill_uid, skill_props, TENANT_ID)
                _merge_rel(session, topic_uid, "REQUIRES_SKILL", skill_uid, TENANT_ID)
            stats["skills"] += 1
            stats["rels"] += 1
            print(f"    [Skill] {skill.get('title', '')}")

            for method in skill.get("methods", []):
                method_uid = method["uid"]
                method_props = {k: v for k, v in method.items() if k not in ("uid", "examples")}

                if not dry_run:
                    _merge_node(session, "Method", method_uid, method_props, TENANT_ID)
                    _merge_rel(session, skill_uid, "HAS_METHOD", method_uid, TENANT_ID)
                stats["methods"] += 1
                stats["rels"] += 1
                print(f"      [Method] {method.get('title', '')}")

                for example in method.get("examples", []):
                    example_uid = example["uid"]
                    example_props = {k: v for k, v in example.items() if k != "uid"}

                    if not dry_run:
                        _merge_node(session, "Example", example_uid, example_props, TENANT_ID)
                        _merge_rel(session, method_uid, "HAS_EXAMPLE", example_uid, TENANT_ID)
                    stats["examples"] += 1
                    stats["rels"] += 1

    return stats


