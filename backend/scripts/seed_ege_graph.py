def seed(exam: str = "all", dry_run: bool = False) -> None:
    repo = Neo4jRepo()
    drv = repo.driver

    try:
        with drv.session() as session:
            total = {"topics": 0, "skills": 0, "methods": 0, "examples": 0, "rels": 0}

            if exam in ("base", "all"):
                print()
                print("─" * 60)
                print("ЕГЭ МАТЕМАТИКА БАЗОВЫЙ 2026")
                print("─" * 60)
                s = _process_exam_data(session, EGE_BASE_DATA, "BASE", dry_run)
                for k in total:
                    total[k] += s[k]

            if exam in ("prof", "all"):
                print()
                print("─" * 60)
                print("ЕГЭ МАТЕМАТИКА ПРОФИЛЬ 2026")
                print("─" * 60)
                s = _process_exam_data(session, EGE_PROF_DATA, "PROF", dry_run)
                for k in total:
                    total[k] += s[k]

            print()
            print("─" * 60)
            print("Патч: переименование legacy-тем в общие названия")
            print("─" * 60)
            _patch_legacy_titles(session, dry_run)

    finally:
        repo.close()

    mode = "(DRY RUN)" if dry_run else "(APPLIED)"
    print()
    print("=" * 60)
    print(f"Seed ЕГЭ завершён {mode}")
    print(f"  Topics   : {total['topics']}")
    print(f"  Skills   : {total['skills']}")
    print(f"  Methods  : {total['methods']}")
    print(f"  Examples : {total['examples']}")
    print(f"  Relations: {total['rels']}")
    print("=" * 60)


