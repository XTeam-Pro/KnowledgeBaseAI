def _patch_legacy_titles(session, dry_run: bool) -> None:
    for uid, new_title, new_desc in _LEGACY_TITLE_PATCHES:
        if not dry_run:
            session.run(
                "MATCH (n {uid: $uid, tenant_id: $tid}) SET n.title = $t, n.description = $d",
                uid=uid, tid=TENANT_ID, t=new_title, d=new_desc,
            )
        print(f"  [patch] {uid} → {new_title}")


def seed(dry_run: bool = False) -> None:
    repo = Neo4jRepo()
    drv = repo.driver  # raw Neo4j driver

    topics_created = 0
    skills_created = 0
    methods_created = 0
    examples_created = 0
    rels_created = 0

    try:
        with drv.session() as session:
            for entry in OGE_DATA:
                topic = entry["topic"]
                topic_uid = topic["uid"]

                if not dry_run:
                    topic_props = {k: v for k, v in topic.items() if k != "uid"}
                    _merge_node(session, "Topic", topic_uid, topic_props, TENANT_ID)
                topics_created += 1
                print(f"  [Topic] {topic_uid}: {topic.get('title', '')}")

                for skill in entry.get("skills", []):
                    skill_uid = skill["uid"]
                    skill_props = {k: v for k, v in skill.items() if k not in ("uid", "methods")}

                    if not dry_run:
                        _merge_node(session, "Skill", skill_uid, skill_props, TENANT_ID)
                        _merge_rel(session, topic_uid, "REQUIRES_SKILL", skill_uid, TENANT_ID)
                    skills_created += 1
                    rels_created += 1
                    print(f"    [Skill] {skill_uid}: {skill.get('title', '')}")

                    for method in skill.get("methods", []):
                        method_uid = method["uid"]
                        method_props = {k: v for k, v in method.items() if k not in ("uid", "examples")}

                        if not dry_run:
                            _merge_node(session, "Method", method_uid, method_props, TENANT_ID)
                            _merge_rel(session, skill_uid, "HAS_METHOD", method_uid, TENANT_ID)
                        methods_created += 1
                        rels_created += 1
                        print(f"      [Method] {method_uid}: {method.get('title', '')}")

                        for example in method.get("examples", []):
                            example_uid = example["uid"]
                            example_props = {k: v for k, v in example.items() if k != "uid"}

                            if not dry_run:
                                _merge_node(session, "Example", example_uid, example_props, TENANT_ID)
                                _merge_rel(session, method_uid, "HAS_EXAMPLE", example_uid, TENANT_ID)
                            examples_created += 1
                            rels_created += 1
                            print(f"        [Example] {example_uid}")

            print()
            print("─" * 60)
            print("Патч: переименование legacy-тем в общие названия")
            print("─" * 60)
            _patch_legacy_titles(session, dry_run)

    finally:
        repo.close()

    mode = "(DRY RUN)" if dry_run else "(APPLIED)"
    print()
    print(f"{'=' * 60}")
    print(f"Seed ОГЭ завершён {mode}")
    print(f"  Topics   : {topics_created}")
    print(f"  Skills   : {skills_created}")
    print(f"  Methods  : {methods_created}")
    print(f"  Examples : {examples_created}")
    print(f"  Relations: {rels_created}")
    print(f"{'=' * 60}")


