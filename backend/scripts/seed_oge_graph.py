def _merge_node(session, label: str, uid: str, props: dict, tenant_id: str) -> None:
    """MERGE узла по uid и tenant_id, затем SET всех свойств."""
    all_props = {
        "uid": uid,
        "type": label,
        "tenant_id": tenant_id,
        "lifecycle_status": "ACTIVE",
        "updated_at": NOW_MS,
        **props,
    }
    q = (
        f"MERGE (n:{label} {{uid: $uid, tenant_id: $tenant_id}}) "
        "SET n += $props"
    )
    session.run(q, uid=uid, tenant_id=tenant_id, props=all_props)


def _merge_rel(session, from_uid: str, rel_type: str, to_uid: str, tenant_id: str) -> None:
    """MERGE связи между двумя узлами."""
    q = (
        f"MATCH (a {{uid: $from_uid, tenant_id: $tid}}), (b {{uid: $to_uid, tenant_id: $tid}}) "
        f"MERGE (a)-[:{rel_type}]->(b)"
    )
    session.run(q, from_uid=from_uid, to_uid=to_uid, tid=tenant_id)


