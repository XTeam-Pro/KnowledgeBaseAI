def seed(dry_run: bool = False) -> None:
    repo = Neo4jRepo()
    drv = repo.driver

    stats = {"topics": 0, "skills": 0, "methods": 0, "examples": 0, "rels": 0, "deleted_rels": 0}

    try:
        with drv.session() as session:
            for entry in DATA:
                topic_uid = entry["topic_uid"]
                remove_old = entry.get("remove_all_old_skills", False)

                print(f"\n[Topic] {topic_uid}")

                # Удаляем старые нерелевантные связи
                if remove_old:
                    deleted = _delete_all_skill_rels(session, topic_uid, dry_run)
                    stats["deleted_rels"] += deleted

                stats["topics"] += 1

                for skill in entry.get("skills", []):
                    skill_uid = skill["uid"]
                    skill_props = {k: v for k, v in skill.items() if k not in ("uid", "methods")}

                    print(f"  [Skill] {skill_uid}: {skill.get('title', '')}")

                    if not dry_run:
                        _merge_node(session, "Skill", skill_uid, skill_props)
                        _merge_rel(session, topic_uid, "REQUIRES_SKILL", skill_uid)
                    stats["skills"] += 1
                    stats["rels"] += 1

                    for method in skill.get("methods", []):
                        method_uid = method["uid"]
                        method_props = {k: v for k, v in method.items() if k not in ("uid", "examples")}

                        print(f"    [Method] {method_uid}: {method.get('title', '')}")

                        if not dry_run:
                            _merge_node(session, "Method", method_uid, method_props)
                            _merge_rel(session, skill_uid, "HAS_METHOD", method_uid)
                        stats["methods"] += 1
                        stats["rels"] += 1

                        for example in method.get("examples", []):
                            ex_uid = example["uid"]
                            ex_props = {k: v for k, v in example.items() if k != "uid"}

                            if not dry_run:
                                _merge_node(session, "Example", ex_uid, ex_props)
                                _merge_rel(session, method_uid, "HAS_EXAMPLE", ex_uid)
                            stats["examples"] += 1
                            stats["rels"] += 1

    finally:
        repo.close()

    mode = "(DRY RUN)" if dry_run else "(ПРИМЕНЕНО)"
    print(f"\n{'='*60}")
    print(f"Batch 1 {mode}")
    print(f"  Тем обработано   : {stats['topics']}")
    print(f"  Навыков создано  : {stats['skills']}")
    print(f"  Методов создано  : {stats['methods']}")
    print(f"  Примеров создано : {stats['examples']}")
    print(f"  Связей создано   : {stats['rels']}")
    print(f"  Связей удалено   : {stats['deleted_rels']}")
    print(f"{'='*60}")


def main():
    parser = argparse.ArgumentParser(description="Fix audit batch 1: первые 20 критических тем")
    parser.add_argument("--dry-run", action="store_true", help="Только просмотр")
    args = parser.parse_args()
    seed(dry_run=args.dry_run)


if __name__ == "__main__":
    main()
